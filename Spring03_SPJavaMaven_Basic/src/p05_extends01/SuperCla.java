package p05_extends01;

public class SuperCla {

	private String name;
	private int age;
	
	public SuperCla() {
		System.out.println("수퍼클래스 생성자");
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
}// class END
