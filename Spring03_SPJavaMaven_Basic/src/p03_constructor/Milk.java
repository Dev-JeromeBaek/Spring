package p03_constructor;

public class Milk {
	
	private String name;
	private int price;
	
	public Milk(String name, int price) {
		super();
		this.name = name;
		this.price = price;
	}
	
	
	public String getName() {
		return name;
	}
	public int getPrice() {
		return price;
	}
	
	
	
}// class END
